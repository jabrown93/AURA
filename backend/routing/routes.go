// @securityDefinitions.apikey BearerAuth
// @in header
// @name Authorization
// @description JWT Token Authentication using the Bearer scheme. Example: "Authorization: Bearer {token}"
func AddRoutes(r *chi.Mux) {
	// If the config is not valid, only allow access to the /onboarding routes
	if !(config.Loaded && config.Valid) {
		logging.LOGGER.Warn().Timestamp().Bool("loaded", config.Loaded).Bool("valid", config.Valid).Msg("Configuration invalid or not loaded, adding onboarding routes only")
		addOnboardingRoutes(r)
		return
	} else {
		logging.LOGGER.Info().Timestamp().Msg("Configuration loaded and valid, adding full routes")
	}

	// Swagger Docs Route
	r.Get("/swagger/*", httpSwagger.WrapHandler)

	// Base Routes
	r.Get("/", routes_base.HealthCheck)
	r.Get("/health", routes_base.HealthCheck)

	r.Route("/api", func(r chi.Router) {
		///////////////////
		// Public Routes
		//////////////////

		// Login - Obtain JWT Token
		r.Post("/login", routes_auth.AttemptLogin)

		// Search - Public Search Endpoint (Media Items, Saved Sets and MediUX Users)
		r.Get("/search", routes_search.HandleSearch)

		// Sonarr Webhook Routes - Public since Sonarr/Radarr need to access it without authentication
		r.Post("/sonarr/webhook", routes_sonarr_radarr.SonarrWebhookHandler)

		/////////////////////
		// Protected Routes
		///////////////////
		r.Group(func(r chi.Router) {
			// Use JWT Auth Middleware
			r.Use(jwtauth.Verifier(routes_auth.TokenAuth))
			r.Use(middleware.Authenticator)

			// Config Routes
			r.Route("/config", func(r chi.Router) {
				r.Get("/", routes_config.GetAppConfigStatus)
				r.Get("/template-variables", routes_config.GetNotificationTemplateVariables)
				r.Post("/", routes_config.UpdateAppConfig)
				r.Patch("/", routes_config.ReloadAppConfig)
			})

			// Database Routes
			r.Route("/db", func(r chi.Router) {
				r.Get("/", routes_db.GetAllItems)
				r.Post("/", routes_db.AddNewItemToDB)
				r.Patch("/", routes_db.UpdateItemInDB)
				r.Delete("/", routes_db.DeleteItemFromDB)
				r.Patch("/ignore", routes_db.IgnoreItemInDB)
				r.Patch("/ignore/stop", routes_db.StopIgnoringItemInDB)
				r.Post("/force-check", routes_db.AutoDownloadForceCheck)
			})

			// Download Routes
			r.Route("/download", func(r chi.Router) {
				// Download
				r.Post("/image/item", routes_download.DownloadImageFileForMediaItem)
				r.Post("/image/collection", routes_download.DownloadImageFileForCollectionItem)

				// Download Queue Routes
				r.Route("/queue", func(r chi.Router) {
					r.Get("/", routes_download.GetDownloadQueueStatus)
					r.Get("/item", routes_download.GetAllDownloadQueueItems)
					r.Post("/item", routes_download.AddItemToDownloadQueue)
					r.Post("/item/retry", routes_download.RetryItemInDownloadQueue)
					r.Delete("/item", routes_download.RemoveItemFromDownloadQueue)
				})
			})

			// Image Routes
			r.Route("/images", func(r chi.Router) {
				r.Get("/media/item", routes_images.GetMediaItemImage)
				r.Get("/media/collection", routes_images.GetCollectionItemImage)
				r.Get("/mediux/item", routes_images.GetMediuxImage)
				r.Get("/mediux/avatar", routes_images.GetMediuxAvatarImage)
				r.Get("/kometa/item", routes_images.GetKometaImage)
				r.Delete("/temp", routes_images.DeleteTempImages)
			})

			// Jobs Routes
			r.Route("/jobs", func(r chi.Router) {
				r.Get("/", routes_jobs.GetAllJobs)
				r.Post("/", routes_jobs.RunJob)
			})

			// Kometa Routes
			r.Route("/kometa", func(r chi.Router) {
				r.Post("/import", routes_kometa.TriggerKometaImport)
				r.Get("/import", routes_kometa.GetKometaImportStatus)
			})

			// Labels & Tags Route
			r.Post("/labels-tags", routes_labels_tags.ApplyLabelsAndTagsToItem)

			// Logging Routes
			r.Route("/logs", func(r chi.Router) {
				r.Get("/", routes_logging.GetLogContents)
				r.Delete("/", routes_logging.ClearLogFiles)
			})

			// Plex OAuth Routes
			r.Get("/oauth/plex", routes_plex.GetPlexPinAndID)
			r.Post("/oauth/plex", routes_plex.CheckAuthStatusWithPlex)

			// Media Server Routes
			r.Route("/mediaserver", func(r chi.Router) {
				r.Get("/libraries", routes_ms.GetLibrarySections)
				r.Post("/libraries/options", routes_ms.GetLibrarySectionOptions)
				r.Get("/library/items", routes_ms.GetLibrarySectionItems)
				r.Get("/item", routes_ms.GetMediaItemDetails)
				r.Get("/collections", routes_ms.GetMovieCollections)
				r.Get("/collections/item", routes_ms.GetAllCollectionChildrenItems)
				r.Patch("/rate", routes_ms.RateMediaItem)
				r.Post("/refresh", routes_ms.RefreshMediaItemMetadata)
			})

			// MediUX Routes
			r.Route("/mediux", func(r chi.Router) {
				r.Get("/user", routes_mediux.GetUserFollowingAndHiding)
				r.Get("/set", routes_mediux.GetSetByID)
				r.Get("/sets/item", routes_mediux.GetItemSets)
				r.Get("/sets/user", routes_mediux.GetAllUserSets)
			})

			// Validation Routes
			r.Route("/validate", func(r chi.Router) {
				r.Post("/mediux", routes_validation.ValidateMediuxInfo)
				r.Post("/mediaserver", routes_validation.ValidateMediaServerInfo)
				r.Post("/sonarr", routes_validation.ValidateSonarrRadarrInfo)
				r.Post("/radarr", routes_validation.ValidateSonarrRadarrInfo)
				r.Post("/notifications", routes_validation.SendTestNotification)
			})
		})
	})
}

func addOnboardingRoutes(r *chi.Mux) {

	// Base Routes
	r.Get("/", routes_base.HealthCheck)
	r.Get("/health", routes_base.HealthCheck)

	r.Route("/api", func(r chi.Router) {
		///////////////////
		// Public Routes
		//////////////////

		// Config Routes
		r.Route("/config", func(r chi.Router) {
			r.Get("/", routes_config.GetAppConfigStatus)
			r.Get("/template-variables", routes_config.GetNotificationTemplateVariables)
			r.Post("/", routes_config.UpdateAppConfig)
		})

		// Plex OAuth Routes
		r.Get("/oauth/plex", routes_plex.GetPlexPinAndID)
		r.Post("/oauth/plex", routes_plex.CheckAuthStatusWithPlex)

		// Logging Routes
		r.Route("/logs", func(r chi.Router) {
			r.Get("/", routes_logging.GetLogContents)
			r.Delete("/", routes_logging.ClearLogFiles)
		})

		r.Post("/mediaserver/libraries/options", routes_ms.GetLibrarySectionOptions)

		// Validation Routes
		r.Route("/validate", func(r chi.Router) {
			r.Post("/mediux", routes_validation.ValidateMediuxInfo)
			r.Post("/mediaserver", routes_validation.ValidateMediaServerInfo)
			r.Post("/sonarr", routes_validation.ValidateSonarrRadarrInfo)
			r.Post("/radarr", routes_validation.ValidateSonarrRadarrInfo)
			r.Post("/notifications", routes_validation.SendTestNotification)
			//r.Post("/database", routes_mediux.ValidateDatabaseInfo)
		})

		// Finalize Onboarding Route
		r.Post("/onboarding/finalize", func(w http.ResponseWriter, r *http.Request) {
			ctx, ld := logging.CreateLoggingContext(r.Context(), r.URL.Path)
			logAction := ld.AddAction("Finalize Onboarding", logging.LevelInfo)
			ctx = logging.WithCurrentAction(ctx, logAction)

			if !(config.Loaded && config.Valid) {
				logAction.SetError("Configuration is not valid or not loaded", "", nil)
				ld.Status = logging.StatusError
				httpx.SendResponse(w, ld, nil)
				return
			}

			triggered := false
			onboardingFinalizeOnce.Do(func() {
				triggered = true
				if OnboardingComplete != nil {
					logging.LOGGER.Info().Timestamp().Msg("Onboarding finalized, executing OnboardingComplete function")
					go OnboardingComplete()
				}
			})

			httpx.SendResponse(w, ld, map[string]any{
				"onboarding_finalized": triggered,
			})
		})
	})
}
